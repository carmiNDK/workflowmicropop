package service;

public class Client {

	private String nomclient;
	private String postnomclient;
	private String prenomclient;
	private String photoclient;

	public String getNomclient() {
		return nomclient;
	}

	public void setNomclient(String nomclient) {
		this.nomclient = nomclient;
	}

	public String getPostnomclient() {
		return postnomclient;
	}

	public void setPostnomclient(String postnomclient) {
		this.postnomclient = postnomclient;
	}

	public String getPrenomclient() {
		return prenomclient;
	}

	public void setPrenomclient(String prenomclient) {
		this.prenomclient = prenomclient;
	}

	public String getPhotoclient() {
		return photoclient;
	}

	public void setPhotoclient(String photoclient) {
		this.photoclient = photoclient;
	}

}
